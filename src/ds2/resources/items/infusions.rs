impl fmt::Display for Infusions {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        let name = match self {
            Self::Normal => "Normal",
            Self::Fire => "Fire",
            Self::Magic => "Magic",
            Self::Lightning => "Lightning",
            Self::Dark => "Dark",
            Self::Poison => "Poison",
            Self::Bleed => "Bleed",
            Self::Raw => "Raw",
            Self::Enchanted => "Enchanted",
            Self::Mundane => "Mundane",
        };
        write!(f, "{}", name)
    }
}

