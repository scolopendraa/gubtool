use once_cell::sync::Lazy;

use crate::er::{resources::items::Item, utils::is_version_dlc_compat};

#[derive(Clone, Copy)]
pub struct Aow {
    pub id: i64,
    pub name: &'static str,
    pub available_affinities: u16,
    pub weapon_type_flags: [u8; 6],
    pub dlc: bool,
}

#[derive(Clone, Copy)]
pub struct Affinity {
    pub name: &'static str,
    pub flag: u16,
    pub id_offset: i64,
}

impl Aow {
    pub fn supports_item(self, item: Item) -> bool {
        if !matches!(item.gem_mount_type, Some(1) | Some(2)) {
            return false;
        }
        item.weapon_type
            .and_then(weapon_type_to_bit)
            .map(|bit_index| self.weapon_type_flags[bit_index / 8] & (1 << (bit_index % 8)) != 0)
            .unwrap_or(false)
    }

    pub fn supports_affinity(self, flag: u16) -> bool {
        (self.available_affinities & flag) != 0
    }
}

fn weapon_type_to_bit(weapon_type: u8) -> Option<usize> {
    Some(match weapon_type {
        1 => 0,
        3 => 1,
        5 => 2,
        7 => 3,
        9 => 4,
        0xB => 5,
        0xD => 6,
        0xE => 7,
        0xF => 8,
        0x10 => 9,
        0x11 => 10,
        0x13 => 11,
        0x15 => 12,
        0x17 => 13,
        0x18 => 14,
        0x19 => 15,
        0x1B => 16,
        0x1C => 17,
        0x1D => 18,
        0x1F => 19,
        0x23 => 20,
        0x25 => 21,
        0x27 => 22,
        0x29 => 23,
        0x32 => 24,
        0x33 => 25,
        0x35 => 26,
        0x37 => 27,
        0x38 => 28,
        0x39 => 29,
        0x3B => 30,
        0x3D => 31,
        0x41 => 32,
        0x43 => 33,
        0x45 => 34,
        0x58 => 36,
        0x59 => 37,
        0x5A => 38,
        0x5C => 40,
        0x5D => 41,
        0x5E => 42,
        0x5F => 43,
        _ => return None,
    })
}

